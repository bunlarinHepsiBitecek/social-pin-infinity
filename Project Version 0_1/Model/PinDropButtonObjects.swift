import UIKit

class PinDropButtonObjects {
    
    var pinButtonOnlyMe : CGPoint {
        
        get {
            
            return self.pinButtonOnlyMe
            
        }
        
        set(inputCGPoing) {
            
            self.pinButtonOnlyMe = inputCGPoing
        }
        
    }
    
    var pinButtonAllFriends : CGPoint {
        
        get {
            
            return self.pinButtonAllFriends
            
        }
        
        set(inputCGPoint) {
            
            self.pinButtonAllFriends = inputCGPoint
            
        }
        
    }
    
    var pinButtonSpecialPersonOrGroup : CGPoint {
        
        get {
            
            return self.pinButtonSpecialPersonOrGroup
            
        }
        
        set(inputCGPoint) {
            
            self.pinButtonSpecialPersonOrGroup = inputCGPoint
            
        }
        
    }
    
    
}
